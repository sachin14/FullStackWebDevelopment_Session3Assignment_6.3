//Code for celsius to farenheit
var temperatureInCelsius = 40;
var celsiusToFarenheit = (temperatureInCelsius * 9)/5 + 32;
//Log celsius to farenheit to console
console.log('Temperature of ' + temperatureInCelsius + ' celsius, will be ' + celsiusToFarenheit + ' farenheit');

//code for farenheit to celsius
var temperatureInFarenheit = 104;
var farenheitToCelsius = ((temperatureInFarenheit - 32)* 5)/9;
//Log farenheit to celsius to console
console.log('Temperature of ' + temperatureInFarenheit + ' farenheit, will be ' + farenheitToCelsius + ' celsius');
